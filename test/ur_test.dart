      final message = makeMessageUR(32767);
      const maxFragmentLength = 1000;
      const firstSeqNum = 100;
      final encoder = UREncoder(message, maxFragmentLength: maxFragmentLength, firstSeqNum: firstSeqNum);
      final decoder = URDecoder();

      do {
        final part = encoder.nextPart();
        decoder.receivePart(part);
      } while (!decoder.isComplete());

      if (decoder.isSuccess()) {
        expect(decoder.resultUR().equals(message), isTrue);
      } else {
        print('decoder.resultError(): ${decoder.resultError()}');
        expect(false, isTrue);
      }
    });

